package numberPuzzleOOP;


public class Player {

	   private int x;
	   private int y;
	   
	   public void setPosition(int x, int y) {
	      this.x = x;
	      this.y = y; //set
	   }
	   
	   public int getX() 
	   {
	      return x;
	   }
	   
	   public int getY() 
	   {
	      return y;
	   }
	}